// Q. WAP that prints You have a fever if your temperature is above 100 and otherwise prints You don't have a fever.



public class Question12 {
    public static void main(String args[]) {

        double temp = 103.5;

        if (temp > 100) {
            System.out.println("OMG! You have a fever");
        } 
        else {
            System.out.println("Relax! You don't have a fever");
        }
    }
}
