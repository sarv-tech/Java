public class Variables {
    public static void main(String[] args) {
        
        int a = 5;
        int b = 6;

        System.out.println(a);
        System.out.println(b);
        String name = "Sarvesh";
        System.out.println(name);

        a = 10;
        System.out.print(a + b);
    }
}

// a & b are identifiers hence its a variable.
