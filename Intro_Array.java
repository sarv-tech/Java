/*

Assume humko store karana hai "apple", "mango", 4.9, 8 so hum inn charo ko milakar ek array nai bana sakte kyuki "apple" aur "mango" ka type string hai, 4.9 ek floating value hai, 8 ek integer value hai.

But, agar humko numbers hi store karane hai eg.:- 1,2,3,4,5,6 ya humko string store karana hai eg.:- "apple", "mango" ya humko floating varaible store karane ho eg.:- 1.1, 4.9, 8.6, 2.8

isme hum 1, 2, 3, 4, 5, 6 ----> Integer type ka array bana sakte
isme hum "mango", "apple" ------> String type ka array bana sakte
isme hum 1.1, 4.9, 8.6, 2.8 -----> Floating type ka array bana sakte
isme hum 'a', 'b', 'c', 'd' -----> character type ka array bana sakte

Arrays joh hote usme same type ka data store hona chaiye

Continguous memory mein store hote hai ----> ek ke baad ek ke baad lagadar elements store horhe hote

Note:- Here char size is taken as 1 byte for simplicity. java char takes up 2 bytes in memory.
    
*/